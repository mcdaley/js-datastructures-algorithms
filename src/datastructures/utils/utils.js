/**
 * Uses JavaScript's default equals to compare to objects. Returns true if 
 * the objects are equals, otherwise returns false
 * @param   {Object}  a 
 * @param   {Object}  b
 * @returns {Boolean} 
 */
export function defaultEquals(a, b) {
  return a === b
}